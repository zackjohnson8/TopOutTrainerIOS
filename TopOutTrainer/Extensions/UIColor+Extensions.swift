import Foundation
import UIKit

public extension UIColor
{
    static func primaryColor() -> UIColor
    {
        return UIColor.systemGray5
    }
    
    static func selectionGreen() -> UIColor
    {
        return UIColor(red: 0/255, green: 199/255, blue: 9/255, alpha: 1.0)
    }
}
